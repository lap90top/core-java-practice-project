package variable;

public class VariableReuse {

    //main method to run program
    public static void main(String[] args) {

        // Syntax for Variable : DataType  variable_name = value;

        // Datatype : String

        String city = "Agra";

        System.out.println("City is :"+city); // result Agra

        // reuse of variable (like city)

        city = "Jaipur";

        System.out.println(" Value of reuse variable city is :"+city); // result Jaipur


        // Datatype :int

        int a = 10;

        System.out.println("Value of variable a is :"+a); // result 10


        a = 20;

        System.out.println("Value of reuse variable a is :"+a); // result 20







    }

}
